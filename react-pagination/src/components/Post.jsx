import PropTypes from 'prop-types'

const Post = ({ posts, loading }) => {
  if (loading) {
    return <h2>Loading...</h2>;
  }

  return (
    <ul className="list-group mb-4">
      {posts.map((post) => (
        <li key={post.id} className="list-group-item">
          {post.title}
        </li>
      ))}
    </ul>
  );
};

Post.propTypes = {
    posts: PropTypes.array,
    loading: PropTypes.bool
}

export default Post

